mod psp;

fn main() -> Result<(), std::io::Error> {
    let artifact_dir = psp::cmake_build()?;
    psp::cmake_link_deps(&artifact_dir)?;
    psp::cxx_bridge_build();
    psp::install_docs();
    Ok(())
}
