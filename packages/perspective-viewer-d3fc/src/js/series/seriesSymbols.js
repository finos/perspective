import * as d3 from "d3";

const symbols = [
    d3.symbolCircle,
    d3.symbolCross,
    d3.symbolDiamond,
    d3.symbolSquare,
    d3.symbolStar,
    d3.symbolTriangle,
    d3.symbolWye,
];

export function fromDomain(domain) {
    return domain.length > 1
        ? d3.scaleOrdinal().domain(domain).range(symbols)
        : null;
}
